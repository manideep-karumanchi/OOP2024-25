class VariableDemo{
	public static void main(String[] args){
		byte byteVariable = 100;
		System.out.println("byteVariable = "+byteVariable);
		byteVariable = (byte)280;
		System.out.println("byteVariable = "+byteVariable);
		
		short $hortVariable = 200;
		System.out.println("$hortVariable = "+$hortVariable);
		
		int _intVariable = 37800;
		System.out.println("_intVariable = "+_intVariable);
		
		long long_Variable = 20000000000L;
		System.out.println("long_Variable = "+long_Variable);
		
		char ch = '\u0c05', ch1 = '\u0c06';
		System.out.println("Character Variables: "+ch+" "+ch1);
		
		
	}
}